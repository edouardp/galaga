@app.cell
def _(mo):
    diagram_direction = mo.ui.dropdown(
        options=["TD", "LR", "BT", "RL"],
        value="LR",
        label="direction",
    )
    compact_diagram = mo.ui.switch(value=False, label="compact tree")
    annotate_values = mo.ui.switch(value=True, label="show values")
    mo.hstack([diagram_direction, compact_diagram, annotate_values])
    return annotate_values, compact_diagram, diagram_direction


@app.cell
def _(
    annotate_values,
    compact_diagram,
    diagram_direction,
    mo,
    mv_to_mermaid,
    rotated_u,
    rotation_plane,
    theta,
    u,
):
    symbol_environment = {
        "u": u,
        "B": rotation_plane,
        "theta": theta,
    }
    interactive_mermaid = mv_to_mermaid(
        rotated_u,
        direction=diagram_direction.value,
        compact=compact_diagram.value,
        show_values=annotate_values.value,
        environment=symbol_environment,
    )
    mo.mermaid(interactive_mermaid)
    return interactive_mermaid, symbol_environment


