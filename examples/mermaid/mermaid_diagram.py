@app.cell
def _(Notation, algebra, expr_to_mermaid, rotated_u, symbol_environment):
    short_functional_presentation = algebra.presentation.with_notation(
        Notation.functional(short=True)
    )
    functional_mermaid = expr_to_mermaid(
        rotated_u.expr,
        presentation=short_functional_presentation,
        direction="TD",
        show_values=True,
        algebra=algebra,
        environment=symbol_environment,
    )
    return (functional_mermaid,)


@app.cell
def _(functional_mermaid, mo):
    mo.mermaid(functional_mermaid, theme="neutral")
    return


